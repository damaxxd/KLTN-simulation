"""
plot_results.py
---------------

Khối 11: Plotting and result export

Input:
    - CSV file generated by simulation_main.py

Output:
    - publication-style plots saved in results/figures

Usage:
    python -m src.plot_results
"""

from __future__ import annotations

from pathlib import Path
import sys

import pandas as pd
import matplotlib.pyplot as plt

if __package__:
    sys.path.insert(0, str(Path(__file__).resolve().parent))

from params import N_MC


RESULTS_FILE = Path("results/simulation_results.csv")
KQ2_COMPARISON_FILE = Path("results/kq2_qoe_scheme_comparison.csv")
KQ5_REFERENCE_FILE = Path("results/kq5_psnr_reference_comparison.csv")
FIG_DIR = Path("results/figures")


def load_results(csv_path: Path = RESULTS_FILE) -> pd.DataFrame:
    """
    Load simulation CSV.

    Input:
        csv_path : path to results CSV

    Output:
        pandas DataFrame
    """
    if not csv_path.exists():
        raise FileNotFoundError(
            f"Results file not found: {csv_path}\n"
            f"Please run simulation_main.py first."
        )
    return pd.read_csv(csv_path)


def ensure_figure_dir() -> None:
    """Create figure output directory if needed."""
    FIG_DIR.mkdir(parents=True, exist_ok=True)


def load_optional_kq2_comparison(csv_path: Path = KQ2_COMPARISON_FILE) -> pd.DataFrame | None:
    """
    Load KQ2 scheme comparison CSV if it has been generated.
    """
    if not csv_path.exists():
        return None
    return pd.read_csv(csv_path)


def load_optional_kq5_reference(csv_path: Path = KQ5_REFERENCE_FILE) -> pd.DataFrame | None:
    """
    Load KQ5 PSNR reference comparison CSV if it has been generated.
    """
    if not csv_path.exists():
        return None
    return pd.read_csv(csv_path)


def save_current_figure(filename: str) -> Path:
    """
    Save current matplotlib figure.

    Input:
        filename : output image filename

    Output:
        saved path
    """
    ensure_figure_dir()
    out_path = FIG_DIR / filename
    plt.tight_layout()
    plt.savefig(out_path, dpi=300, bbox_inches="tight")
    return out_path


def plot_kq1_power_vs_snr(df: pd.DataFrame) -> Path:
    """
    KQ1: Average optimal power allocation versus SNR.

    Curves:
        Avg_Pp, Avg_Psc, Avg_Psp
    """
    plt.figure(figsize=(7, 5))
    plt.plot(df["SNR_dB"], df["Avg_Pp"], marker="o", label="PU power $P_p$")
    plt.plot(df["SNR_dB"], df["Avg_Psc"], marker="s", label="SU common $P_{s,c}$")
    plt.plot(df["SNR_dB"], df["Avg_Psp"], marker="^", label="SU private $P_{s,p}$")

    plt.xlabel("SNR (dB)")
    plt.ylabel("Average Power (W)")
    plt.title("KQ1: Average Optimal Power Allocation vs SNR")
    plt.grid(True)
    plt.legend()

    return save_current_figure("KQ1_power_vs_snr.png")


def plot_kq2_qoe_sys_vs_snr(
    df: pd.DataFrame,
    comparison_df: pd.DataFrame | None = None,
) -> Path:
    """
    KQ2: System QoE versus SNR.

    If the scheme-comparison CSV exists, plot Proposed, OMA, NOMA, and
    CR-NOMA curves on the same figure. Otherwise keep the legacy single
    proposed-method curve.
    """
    plt.figure(figsize=(7, 5))

    comparison_columns = [
        "Avg_QoE_sys_Proposed",
        "Avg_QoE_sys_OMA",
        "Avg_QoE_sys_NOMA",
        "Avg_QoE_sys_CR_NOMA",
    ]

    if comparison_df is not None and all(col in comparison_df.columns for col in comparison_columns):
        plt.plot(
            comparison_df["SNR_dB"],
            comparison_df["Avg_QoE_sys_Proposed"],
            marker="o",
            label="Proposed CR-RSMA",
        )
        plt.plot(
            comparison_df["SNR_dB"],
            comparison_df["Avg_QoE_sys_OMA"],
            marker="s",
            label="OMA",
        )
        plt.plot(
            comparison_df["SNR_dB"],
            comparison_df["Avg_QoE_sys_NOMA"],
            marker="^",
            label="NOMA",
        )
        plt.plot(
            comparison_df["SNR_dB"],
            comparison_df["Avg_QoE_sys_CR_NOMA"],
            marker="D",
            label="CR-NOMA",
        )
        title = "KQ2: Average System QoE Comparison vs SNR"
    else:
        plt.plot(df["SNR_dB"], df["Avg_QoE_sys"], marker="o", label="System QoE")
        title = "KQ2: System QoE vs SNR"

    plt.xlabel("SNR (dB)")
    plt.ylabel("Average QoE")
    plt.title(title)
    plt.grid(True)
    plt.legend()

    return save_current_figure("KQ2_qoe_sys_vs_snr.png")


def plot_kq3_qoe_users_vs_snr(df: pd.DataFrame) -> Path:
    """
    KQ3: QoE of PU and SU versus SNR.
    """
    plt.figure(figsize=(7, 5))
    plt.plot(df["SNR_dB"], df["Avg_QoE_p"], marker="o", label="PU QoE")
    plt.plot(df["SNR_dB"], df["Avg_QoE_s"], marker="s", label="SU QoE")

    plt.xlabel("SNR (dB)")
    plt.ylabel("Average QoE")
    plt.title("KQ3: PU and SU QoE vs SNR")
    plt.grid(True)
    plt.legend()

    return save_current_figure("KQ3_qoe_users_vs_snr.png")


def plot_kq5_psnr_vs_snr(
    df: pd.DataFrame,
    reference_df: pd.DataFrame | None = None,
) -> Path:
    """
    KQ5: Average PSNR versus SNR.

    If the KQ5 paper-reference CSV exists, plot the proposed method against
    adapted paper-method baselines. Otherwise keep the legacy PU/SU PSNR plot
    for backwards compatibility.
    """
    plt.figure(figsize=(7, 5))
    reference_columns = [
        "Avg_PSNR_sys_Proposed",
        "Avg_PSNR_sys_Feng_RSMA_UAV",
        "Avg_PSNR_sys_He_CR_NOMA_MTCC",
    ]

    if reference_df is not None and all(col in reference_df.columns for col in reference_columns):
        plt.plot(
            reference_df["SNR_dB"],
            reference_df["Avg_PSNR_sys_Proposed"],
            marker="o",
            label="Proposed CR-RSMA + SVC",
        )
        plt.plot(
            reference_df["SNR_dB"],
            reference_df["Avg_PSNR_sys_Feng_RSMA_UAV"],
            marker="s",
            label="Feng et al, adapted method",
        )
        plt.plot(
            reference_df["SNR_dB"],
            reference_df["Avg_PSNR_sys_He_CR_NOMA_MTCC"],
            marker="^",
            label="He et al, adapted CR-NOMA MTCC",
        )
        title = "KQ5: PSNR-Equivalent Reference Comparison vs SNR"
        plt.figtext(
            0.5,
            0.01,
            "Paper methods are adapted to the thesis channel/power/SVC model; "
            "the original papers optimize throughput and do not report PSNR.",
            ha="center",
            fontsize=8,
        )
    else:
        plt.plot(df["SNR_dB"], df["Avg_PSNR_p"], marker="o", label="PU PSNR")
        plt.plot(df["SNR_dB"], df["Avg_PSNR_s"], marker="s", label="SU PSNR")
        title = "KQ5: PSNR vs SNR"

    plt.xlabel("SNR (dB)")
    plt.ylabel("Average PSNR (dB)")
    plt.title(title)
    plt.grid(True)
    plt.legend()

    return save_current_figure("KQ5_psnr_vs_snr.png")


def plot_kq6_outage_vs_snr(df: pd.DataFrame) -> Path:
    """
    KQ6: PU and SU outage probability versus SNR.
    """
    plt.figure(figsize=(7, 5))
    display_floor = 1.0 / float(N_MC)
    pu_outage_plot = df["PU_Outage"].clip(lower=display_floor)
    su_outage_plot = df["SU_Outage"].clip(lower=display_floor)

    plt.semilogy(
        df["SNR_dB"],
        pu_outage_plot,
        marker="o",
        label="PU Outage Probability",
    )
    plt.semilogy(
        df["SNR_dB"],
        su_outage_plot,
        marker="s",
        label="SU Outage Probability",
    )

    plt.xlabel("SNR (dB)")
    plt.ylabel("Outage Probability")
    plt.title("KQ6: PU/SU Outage Probability")
    plt.ylim(bottom=display_floor)
    plt.grid(True, which="both", linestyle=":", linewidth=0.6)
    plt.legend()

    return save_current_figure("KQ6_outage_vs_snr.png")


def plot_aux_layers_vs_snr(df: pd.DataFrame) -> Path:
    """
    Auxiliary figure:
        Average number of decodable SVC layers versus SNR.
    """
    plt.figure(figsize=(7, 5))
    plt.plot(df["SNR_dB"], df["Avg_Layers_s"], marker="o", label="Avg decoded layers")

    plt.xlabel("SNR (dB)")
    plt.ylabel("Average Decodable Layers")
    plt.title("Auxiliary: Average Decodable SVC Layers vs SNR")
    plt.grid(True)
    plt.legend()

    return save_current_figure("Aux_layers_vs_snr.png")


def main() -> None:
    df = load_results()
    kq2_comparison_df = load_optional_kq2_comparison()
    kq5_reference_df = load_optional_kq5_reference()

    print("=== LOADED RESULTS ===")
    print(df.head())

    if kq2_comparison_df is not None:
        print("\n=== LOADED KQ2 COMPARISON ===")
        print(kq2_comparison_df.head())

    if kq5_reference_df is not None:
        print("\n=== LOADED KQ5 REFERENCE COMPARISON ===")
        print(kq5_reference_df.head())

    paths = []
    paths.append(plot_kq1_power_vs_snr(df))
    paths.append(plot_kq2_qoe_sys_vs_snr(df, kq2_comparison_df))
    paths.append(plot_kq3_qoe_users_vs_snr(df))
    paths.append(plot_kq5_psnr_vs_snr(df, kq5_reference_df))
    paths.append(plot_kq6_outage_vs_snr(df))
    paths.append(plot_aux_layers_vs_snr(df))

    print("\nSaved figures:")
    for p in paths:
        print(f" - {p}")


if __name__ == "__main__":
    main()
